def pivot_scores(rows: list[dict[str, Any]]) -> tuple[list[str], list[str], list[list[float]]]:
    cases = sorted({str(row.get("case_id")) for row in rows if row.get("case_id")})
    policies = []
    for row in rows:
        policy = str(row.get("policy") or row.get("policy_key") or "")
        if policy and policy not in policies:
            policies.append(policy)
    matrix = [[0.0 for _ in cases] for _ in policies]
    for row in rows:
        policy = str(row.get("policy") or row.get("policy_key") or "")
        case = str(row.get("case_id") or "")
        if policy in policies and case in cases:
            matrix[policies.index(policy)][cases.index(case)] = safe_float(row.get("score"))
    return policies, cases, matrix


