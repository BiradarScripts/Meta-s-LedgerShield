def model_size_number(model: str) -> float | None:
    text = model.lower().replace("b", "")
    try:
        return float(text)
    except ValueError:
        return None


def plot_scaling_law(data: dict[str, Any], output_dir: Path, filename: str = "03_scaling_law_score_vs_model_size.png") -> Path:
    rows = []
    for row in numeric_rows(data["matrix"], "mean_score"):
        size = model_size_number(str(row.get("model") or ""))
        if size is not None:
            rows.append((size, float(row["mean_score"]), str(row.get("method")), policy_label(row)))
    if not rows:
        return plot_no_data(output_dir / filename, "Scaling Law")
    plt.figure(figsize=(7.5, 5.0))
    for method in sorted({row[2] for row in rows}):
        subset = sorted([row for row in rows if row[2] == method])
        plt.plot([row[0] for row in subset], [row[1] for row in subset], marker="o", linewidth=2, label=method)
        for size, score, _, label in subset:
            plt.text(size, score + 0.015, label, fontsize=8)
    plt.title("Scaling Law: Score vs Model Size", fontsize=15, weight="bold")
    plt.xlabel("Model size (billions of parameters)")
    plt.ylabel("Mean final score")
    plt.ylim(0, 1.05)
    plt.grid(True, alpha=0.25)
    plt.legend(fontsize=8)
    return save_figure(output_dir / filename)


