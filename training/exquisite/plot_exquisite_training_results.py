def plot_teacher_gap_closure(data: dict[str, Any], output_dir: Path, filename: str = "05_teacher_gap_closure.png") -> Path:
    rows = numeric_rows(data["matrix"], "mean_score")
    base = next((float(row["mean_score"]) for row in rows if row.get("policy_key") == "base_model"), None)
    teacher = next((float(row["mean_score"]) for row in rows if row.get("policy_key") == "teacher_policy"), None)
    if base is None or teacher is None or teacher <= base:
        return plot_no_data(output_dir / filename, "Teacher-Gap Closure", "Base and teacher numeric anchors are required.")
    labels: list[str] = []
    values: list[float] = []
    for row in rows:
        if row.get("policy_key") in {"random_baseline", "naive_baseline", "base_model", "teacher_policy"}:
            continue
        closure = (float(row["mean_score"]) - base) / (teacher - base)
        labels.append(policy_label(row))
        values.append(max(0.0, min(1.0, closure)))
    if not labels:
        return plot_no_data(output_dir / filename, "Teacher-Gap Closure")
    plt.figure(figsize=(8.5, 4.8))
    plt.bar(labels, values, color="#0ea5e9")
    annotate(values)
    plt.title("Teacher-Gap Closure", fontsize=15, weight="bold")
    plt.ylabel("Fraction of base-to-teacher gap closed")
    plt.ylim(0, 1.05)
    plt.xticks(rotation=20, ha="right")
    return save_figure(output_dir / filename)


