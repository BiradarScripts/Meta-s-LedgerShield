def plot_candidate_rank_heatmap(data: dict[str, Any], output_dir: Path, filename: str = "19_selfplay_candidate_rank_heatmap.png") -> Path:
    rows = data["selfplay"]
    cases = sorted({str(row.get("case_id")) for row in rows if row.get("case_id")})
