    plt.axhline(0, color="#111827", linewidth=1)
    plt.title("Per-Case Policy Delta from SFT", fontsize=15, weight="bold")
    plt.ylabel("Score delta")
    plt.xticks(rotation=65, ha="right", fontsize=7)
    return save_figure(output_dir / filename)


