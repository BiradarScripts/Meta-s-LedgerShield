def count_by(rows: list[dict[str, Any]], key: str) -> dict[str, int]:
    counts: dict[str, int] = {}
    for row in rows:
        value = row.get(key)
        if isinstance(value, dict):
            value = value.get("result_class")
        label = str(value or "unknown")
        counts[label] = counts.get(label, 0) + 1
    return counts


