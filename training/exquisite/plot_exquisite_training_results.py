def rolling(values: list[float], window: int = 10) -> list[float]:
    out = []
    for index in range(len(values)):
        chunk = values[max(0, index - window + 1) : index + 1]
        out.append(sum(chunk) / max(len(chunk), 1))
    return out


def rolling_std(values: list[float], window: int = 10) -> list[float]:
    out = []
    for index in range(len(values)):
        chunk = values[max(0, index - window + 1) : index + 1]
        mean = sum(chunk) / max(len(chunk), 1)
        out.append(math.sqrt(sum((value - mean) ** 2 for value in chunk) / max(len(chunk), 1)))
    return out


