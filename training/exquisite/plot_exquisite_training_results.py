def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Render Exquisite visualization pack.")
    parser.add_argument("--artifact-root", type=Path, default=EXQUISITE_ROOT)
    parser.add_argument("--report-dir", type=Path, default=EXQUISITE_ROOT / "reports")
    parser.add_argument("--output-dir", type=Path, default=EXQUISITE_ROOT / "plots")
    return parser.parse_args()


def load_data(artifact_root: Path, report_dir: Path) -> dict[str, Any]:
    grpo_rows: list[dict[str, Any]] = []
    for path in sorted(artifact_root.glob("*/grpo_reward_history.csv")):
        for row in read_csv(path):
            row["run_name"] = path.parent.name
            grpo_rows.append(row)
    return {
        "matrix": read_csv(report_dir / "final_policy_matrix.csv"),
        "selfplay": read_jsonl(artifact_root / "selfplay-0.5b" / "selfplay_candidates.jsonl"),
        "preferences": read_jsonl(artifact_root / "selfplay-0.5b" / "falsifier_preferences.jsonl"),
        "grpo_history": grpo_rows,
        "per_case": read_jsonl(report_dir / "per_case_results.jsonl"),
        "per_task": read_csv(report_dir / "per_task_results.csv"),
        "failure_taxonomy": read_json(report_dir / "failure_taxonomy.json", default={}) or {},
        "ablations": read_json(report_dir / "ablation_results.json", default={}) or {},
    }


def save_figure(path: Path) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    plt.tight_layout()
    plt.savefig(path, dpi=170, bbox_inches="tight")
    plt.close()
    return path


def plot_no_data(path: Path, title: str, message: str = "Pending Hugging Face run artifacts.") -> Path:
    plt.figure(figsize=(8.5, 4.8))
    plt.axis("off")
    plt.title(title, fontsize=15, weight="bold")
    plt.text(0.5, 0.5, message, ha="center", va="center", wrap=True, fontsize=11, color="#334155")
    return save_figure(path)


def numeric_rows(matrix: list[dict[str, Any]], key: str = "mean_score") -> list[dict[str, Any]]:
    rows = []
    for row in matrix:
        value = maybe_float(row.get(key))
        if value is None:
            continue
        payload = dict(row)
        payload[key] = value
        rows.append(payload)
    return rows


def policy_label(row: dict[str, Any]) -> str:
    model = str(row.get("model") or "")
    policy = str(row.get("policy") or row.get("policy_key") or "")
    if model and model != "-" and model not in policy:
        return f"{policy} {model}"
    return policy


def annotate(values: list[float]) -> None:
    for index, value in enumerate(values):
        plt.text(index, value + 0.015, f"{value:.2f}", ha="center", va="bottom", fontsize=8)


def plot_final_policy_ladder(data: dict[str, Any], output_dir: Path, filename: str = "01_final_policy_ladder.png") -> Path:
    rows = numeric_rows(data["matrix"], "mean_score")
    if not rows:
        return plot_no_data(output_dir / filename, "Final Policy Ladder")
    labels = [policy_label(row) for row in rows]
    values = [float(row["mean_score"]) for row in rows]
    colors = [POLICY_COLORS.get(str(row.get("policy")), "#64748b") for row in rows]
    plt.figure(figsize=(max(9, len(labels) * 0.9), 5.0))
    plt.plot(range(len(values)), values, color="#94a3b8", linewidth=1.3, zorder=1)
    plt.scatter(range(len(values)), values, s=150, color=colors, zorder=2)
    annotate(values)
    plt.xticks(range(len(labels)), labels, rotation=20, ha="right")
    plt.ylabel("Mean final score")
    plt.ylim(0, max(1.0, max(values) + 0.12))
    plt.title("Final Policy Ladder", fontsize=15, weight="bold")
    plt.grid(True, axis="y", alpha=0.22)
    return save_figure(output_dir / filename)


